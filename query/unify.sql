CREATE TABLE Utente
(
	id_utente char(16) PRIMARY KEY,
	nome varchar(25) NOT NULL,
	cognome varchar(25) NOT NULL,
	nome_arte varchar(50),
	email varchar(80) NOT NULL UNIQUE,
	data_iscrizione date NOT NULL,
	biografia varchar(100),
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%')
	
);

CREATE TABLE Brano
(
	id_brano char(16) PRIMARY KEY,
	id_utente char(16) NOT NULL,
	titolo varchar(25) NOT NULL,
	casa_discografica varchar(25),
	genere char(16),
	durata time NOT NULL,
	FOREIGN KEY (id_utente) REFERENCES Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_utente)=16 and id_utente LIKE 'U%'),
	CHECK(LENGTH(id_brano)=16 AND id_brano LIKE 'B%')
);

CREATE TABLE Inserzionista
(
	partita_iva char(11) PRIMARY KEY,
	telefono varchar(22) NOT NULL,
	email varchar(80) NOT NULL UNIQUE,
	nome varchar(25) NOT NULL,
	CHECK(LENGTH(partita_iva)=11)
);

CREATE TABLE Pubblicità
(
	id_pubblicità char(16) PRIMARY KEY,
	partita_iva varchar(11) NOT NULL,
	durata time NOT NULL,
	banner boolean,
	audio boolean,
	video boolean,
	FOREIGN KEY (partita_iva) REFERENCES Inserzionista(partita_iva)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_pubblicità)=16 AND id_pubblicità LIKE 'P%')
);

CREATE TABLE Album
(
	id_album char(16) PRIMARY KEY,
	titolo varchar(50) NOT NULL,
	data_rilascio date NOT NULL
	CHECK(LENGTH(id_album)=16 AND id_album LIKE 'A%')
);

CREATE TABLE Composizione
(
	id_album char(16) NOT NULL,
	id_utente char(16) NOT NULL,
	PRIMARY KEY(id_album,id_utente),
	FOREIGN KEY (id_album) REFERENCES Album(id_album)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY (id_utente) REFERENCES Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_album)=16 AND id_album LIKE 'A%'),
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%')
);


CREATE TABLE Concerto 
(	
	giorno_ora timestamp NOT NULL,
 	luogo char(16) NOT NULL,
 	biglietti_disponibili integer,
 	PRIMARY KEY(giorno_ora,luogo)
);

CREATE TABLE Pianificazione
(
	id_utente char(16) NOT NULL,
	giorno_ora timestamp NOT NULL,
	luogo char(16) NOT NULL,
	PRIMARY KEY(id_utente,giorno_ora,luogo),
	FOREIGN KEY(id_utente) references Utente(id_utente),
	FOREIGN KEY(giorno_ora,luogo) references Concerto(giorno_ora,luogo),
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%')
);

CREATE TABLE Riproduzione
(
	id_utente CHAR(16) NOT NULL,
	ora_ascolto timestamp NOT NULL,
	id_brano VARCHAR(16) NOT NULL,
	PRIMARY KEY(id_utente,ora_ascolto),
	FOREIGN KEY(id_utente) references Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(id_brano) references Brano(id_brano)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%'),
	CHECK(LENGTH(id_brano)=16 AND id_brano LIKE 'B%')
);

CREATE TABLE Visualizzazione
(
	id_pubblicità char(16) NOT NULL,
	ora_visualizzazione timestamp NOT NULL,
	id_utente char(16) NOT NULL,
	PRIMARY KEY(id_utente,ora_visualizzazione),
	FOREIGN KEY(id_pubblicità) references Pubblicità (id_pubblicità)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(id_utente) references Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%'),
	CHECK(LENGTH(id_pubblicità)=16 AND id_pubblicità LIKE 'P%')
	
);


CREATE TABLE Playlist
(
	titolo_playlist varchar(16) NOT NULL,
	id_utente char(16) NOT NULL, 
	descrizione char(25),
	data_creazione date NOT NULL,
	PRIMARY KEY(titolo_playlist, id_utente),
	FOREIGN KEY(id_utente) references Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%')
);

CREATE TABLE Featuring
(	
	id_utente char(16) NOT NULL,
	id_brano varchar(16) NOT NULL,	
	PRIMARY KEY(id_brano, id_utente),
	FOREIGN KEY(id_utente) references Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(id_brano) references Brano(id_brano)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%'),
	CHECK(LENGTH(id_brano)=16 AND id_brano LIKE 'B%')
);


CREATE TABLE ContieneAlbum 
(
	id_album char(16) NOT NULL,
	id_brano varchar(16) NOT NULL,
	PRIMARY KEY(id_album, id_brano),
	FOREIGN KEY(id_album) references album(id_album)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(id_brano) references Brano(id_brano)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_brano)=16 AND id_brano LIKE 'B%'),
	CHECK(LENGTH(id_album)=16 AND id_album LIKE 'A%')
	
);

CREATE TABLE ContienePlaylist 
(
	titolo_playlist char(16) NOT NULL,
	id_utente char(16) NOT NULL,
	id_brano varchar(16) NOT NULL,
	PRIMARY KEY(titolo_playlist, id_brano, id_utente),
	FOREIGN KEY(id_utente,titolo_playlist) references Playlist(id_utente,titolo_playlist)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(id_brano) references Brano(id_brano)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_brano)=16 AND id_brano LIKE 'B%'),
	CHECK(LENGTH(id_utente)=16 AND id_utente LIKE 'U%')
);


CREATE TABLE Segue
(
	id_follower char(16) NOT NULL,
	id_following char(16) NOT NULL,
	PRIMARY KEY(id_follower,id_following),
	FOREIGN KEY(id_follower) REFERENCES Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(id_following) REFERENCES Utente(id_utente)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CHECK(LENGTH(id_follower)=16 AND id_follower LIKE 'U%'),
	CHECK(LENGTH(id_following)=16 AND id_following LIKE 'U%')
);

